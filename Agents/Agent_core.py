# 3. Node 1: Guardrail Agent
# ==========================================
async def guardrail_node(state: BIAgentState) -> dict:
    print("🛡️ Guardrail Node Triggered")
    system_prompt = get_guardrail_prompt(state.current_tab)
    messages = [SystemMessage(content=system_prompt), HumanMessage(content=state.user_query)]
    
    # Using specific max_tokens for this ultra-fast node
    response = await router_llm.ainvoke(messages)
    clean_result = re.sub(r'<think>.*?</think>', '', response.content, flags=re.DOTALL).strip().upper()
    
    if clean_result not in ["PASS", "FAIL"]:
        clean_result = "PASS" if "PASS" in clean_result else "FAIL"
            
    return {"guardrail_status": clean_result}

