# 7. Node 4: Backend Router & Format Node
# ==========================================
async def format_output_node(state: BIAgentState) -> dict:
    print("🛠️ Format Output Node Triggered")
    payload = {"data_type": state.data_type, "chart_type": state.chart_type, "data": None}
    
    updated_history = state.chat_history.copy()
    updated_history.append({"role": "user", "content": state.user_query})
    ai_memory_text = ""
    
    # Check if the user's query failed the Guardrail Check
    if state.guardrail_status == "FAIL":
        fallback_msg = "I am a specialized Data Assistant. I can only help you query, visualize, and analyze data within this application."
        payload["data"] = fallback_msg
        payload["data_type"] = "message"
        ai_memory_text = fallback_msg

    # Check if the text-to-SQL logic returned a clarification message or failed completely
    elif state.data_type == "message" or (len(state.error_log) > 0 and state.sql_retries >= 2):
        fallback_msg = state.message_to_user if state.data_type == "message" else "I encountered an error generating the data. Please try rephrasing."
        payload["data"] = fallback_msg
        payload["data_type"] = "message"
        ai_memory_text = fallback_msg
        
    elif state.raw_db_response:
        res = state.raw_db_response
        ai_memory_text = f"[Generated {state.data_type} visualization for the user]"
        
        if "sql_data" in res:
            records = res["sql_data"]
            
            if state.data_type == "table":
                payload["data"] = records
                
            elif state.data_type == "metric":
                if records and len(records) > 0:
                    payload["data"] = list(records[0].values())[0]
                else:
                    payload["data"] = 0
                    
            elif state.data_type == "chart":
                categories = []
                series_data = []
                if records and len(records) > 0:
                    keys = list(records[0].keys())
                    cat_key = keys[0]
                    val_key = keys[1] if len(keys) > 1 else keys[0]
                    
                    for row in records:
                        categories.append(str(row[cat_key]))
                        series_data.append(row[val_key])
                        
                payload["data"] = {
                    "categories": categories,
                    "series": [{"name": "Data", "data": series_data}]
                }
                
        else:
            hits = res.get("hits", {}).get("hits", [])
            aggs = res.get("aggregations", {})
            
            if state.data_type == "table":
                payload["data"] = [hit["_source"] for hit in hits]
                
    updated_history.append({"role": "assistant", "content": ai_memory_text})
    
    # Turn count is unconditionally incremented here so ALL interactions are tracked
    return {"final_ui_payload": payload, "chat_history": updated_history, "turn_count": state.turn_count + 1}

# ==========================================
# 8. Node 5: Insights Agent (Triggered for Charts only)
# ==========================================
async def insights_node(state: BIAgentState) -> dict:
    print("💡 Insights Agent Triggered")
    payload = state.final_ui_payload
    updated_history = state.chat_history.copy()
    
    if payload and payload.get("data"):
        data_string = json.dumps(payload["data"])
        prompt = get_insights_prompt(state.user_query, data_string)
        
        response = await router_llm.ainvoke([HumanMessage(content=prompt)])
        clean_insight = re.sub(r'<think>.*?</think>', '', response.content, flags=re.DOTALL).strip()
        
        payload["insight"] = clean_insight
        
        if len(updated_history) > 0 and updated_history[-1]["role"] == "assistant":
            updated_history[-1]["content"] += f" Insight provided: {clean_insight}"
        
    return {"final_ui_payload": payload, "chat_history": updated_history}

# ==========================================
# 9. LangGraph Conditional Routing & Compilation
# ==========================================
def route_guardrail(state: BIAgentState) -> str:
    if state.guardrail_status == "FAIL":
        # Route to formatting node so the state persists properly into Redis
        return "format_output"
    return "context_builder"

def route_sql(state: BIAgentState) -> str:
    if len(state.error_log) > 0 and state.sql_retries < 2:
        print(f"🔄 Retrying JSON Generation... (Attempt {state.sql_retries + 1})")
        return "text_to_sql"
    elif state.data_type == "message" or len(state.error_log) > 0:
        return "format_output"
    return "execute_db"

def route_db_error(state: BIAgentState) -> str:
    if len(state.error_log) > 0 and state.sql_retries < 2:
        print(f"🔄 Retrying DB Query Generation... (Attempt {state.sql_retries + 1})")
        return "text_to_sql"
    return "format_output"

def route_insights(state: BIAgentState) -> str:
    if state.data_type == "chart" and not state.error_log:
        return "insights"
    return END

def create_bi_graph(checkpointer=None):
    workflow = StateGraph(BIAgentState)
    
    workflow.add_node("guardrail", guardrail_node)
    workflow.add_node("context_builder", context_builder_node)
    workflow.add_node("text_to_sql", text_to_sql_node)
    workflow.add_node("execute_db", execute_db_node)
    workflow.add_node("format_output", format_output_node)
    workflow.add_node("insights", insights_node)
    
    workflow.set_entry_point("guardrail")
    workflow.add_conditional_edges("guardrail", route_guardrail)
    workflow.add_edge("context_builder", "text_to_sql")
    workflow.add_conditional_edges("text_to_sql", route_sql)
    workflow.add_conditional_edges("execute_db", route_db_error)
    workflow.add_conditional_edges("format_output", route_insights)
    workflow.add_edge("insights", END)
    
    return workflow.compile(checkpointer=checkpointer)