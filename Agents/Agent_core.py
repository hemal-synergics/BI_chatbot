# 6. Node 3: Database Execution Agent
# ==========================================
async def execute_db_node(state: BIAgentState) -> dict:
    print("📡 Execute DB Node Triggered")
    # Bypass execution if LLM generated a clarification message or failed query
    if state.data_type == "message" or not state.generated_query or not state.db_selected:
        return {"raw_db_response": None}
        
    query_to_execute = state.generated_query
    
    # --- Resolve Database Type & Dynamic Target Credentials ---
    db_type = "elasticsearch"  # Default fallback
    db_credentials = {}
    try:
        # Re-parse the schemas string to find our db_type and credentials
        for block in state.schemas.split("\n---\n"):
            if not block.strip(): continue
            topic_data = json.loads(block)
            for db in topic_data.get("databases", []):
                if db.get("db_name") == state.db_selected:
                    db_type = db.get("db_type")
                    db_credentials = db.get("db_credentials", {})
                    break
    except Exception as e:
        print(f"⚠️ Could not parse schema for db_type. {e}")

    # ==========================================
    # ROUTE A: ELASTICSEARCH EXECUTION
    # ==========================================
    if db_type == "elasticsearch":
        # 🚨 FAIL-SAFE CAPPING: Force maximum 300 records for table data in ES
        if state.data_type == "table" and isinstance(query_to_execute, dict):
            current_size = query_to_execute.get("size", 300)
            try:
                query_to_execute["size"] = min(int(current_size), 300)
            except (ValueError, TypeError):
                query_to_execute["size"] = 300
                
        try:
            # Initialize dynamic client using credentials fetched via topic_selected
            target_es_client = AsyncElasticsearch(
                hosts=[{
                    "host": db_credentials.get("host"), 
                    "port": db_credentials.get("port", 443), 
                    "scheme": db_credentials.get("scheme", "https")
                }],
                basic_auth=(db_credentials.get("user"), db_credentials.get("pass"))
            )
            
            response = await target_es_client.search(index=state.db_selected, body=query_to_execute)
            await target_es_client.close()  # Clean up ephemeral connection
            
            print("✅ Elasticsearch execution successful!")
            return {
                "raw_db_response": response.raw if hasattr(response, 'raw') else response, 
                "error_log": [],
                "generated_query": query_to_execute
            }
        except Exception as e:
            print(f"❌ ES execution failed: {e}")
            return {"error_log": state.error_log + [str(e)], "raw_db_response": None, "sql_retries": state.sql_retries + 1}
            
    # ==========================================
    # ROUTE B: APACHE PARQUET / SQL EXECUTION
    # ==========================================
    elif db_type == "apache_parquet":
        import duckdb
        print(f"📡 Executing SQL query against Apache Parquet via DuckDB")
        
        # 🚨 FAIL-SAFE CAPPING: Force maximum 300 records for table data in SQL
        if state.data_type == "table" and isinstance(query_to_execute, str):
            if "LIMIT" not in query_to_execute.upper():
                query_to_execute += " LIMIT 300"
                
        try:
            # Use in-memory DuckDB instance to avoid heavy Pandas dataframes
            conn = duckdb.connect(':memory:')
            
            # Register the Parquet file dynamically matching the db_credentials
            if "filepath" in db_credentials:
                filepath = db_credentials["filepath"]
                conn.execute(f"CREATE VIEW {state.db_selected} AS SELECT * FROM read_parquet('{filepath}')")
            
            # Execute the LLM generated SQL
            result = conn.execute(query_to_execute)
            
            # Extract columns and rows into a list of dictionaries (Avoids Pandas Memory Leaks)
            columns = [desc[0] for desc in result.description]
            records = [dict(zip(columns, row)) for row in result.fetchall()]
            
            print("✅ Parquet/SQL execution successful!")
            return {
                "raw_db_response": {"sql_data": records},
                "error_log": [],
                "generated_query": query_to_execute
            }
        except Exception as e:
            print(f"❌ SQL execution failed: {e}")
            return {"error_log": state.error_log + [str(e)], "raw_db_response": None, "sql_retries": state.sql_retries + 1}

