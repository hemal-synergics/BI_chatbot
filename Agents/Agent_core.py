# 5. Node 2: Text-to-SQL / Query Router Agent
# ==========================================
async def text_to_sql_node(state: BIAgentState) -> dict:
    print(f"🧠 Text-to-SQL Node Triggered (Attempt {state.sql_retries + 1})")
    error_context = ""
    if state.error_log and len(state.error_log) > 0:
        error_context = f"\n\n[WARNING: Your previous query failed with this error: {state.error_log[-1]}. Please fix the syntax.]"

    augmented_query = state.user_query
    if state.context_summary:
        augmented_query = f"[Previous Conversation Context: {state.context_summary}]\n\nCurrent User Query: {state.user_query}"
        
    augmented_query += error_context

    system_prompt = get_text_to_sql_prompt(state.current_tab, state.schemas)
    messages = [SystemMessage(content=system_prompt), HumanMessage(content=augmented_query)]
    
    response = await coder_llm.ainvoke(messages)
    clean_result = re.sub(r'<think>.*?</think>', '', response.content, flags=re.DOTALL).strip()
    match = re.search(r'\{.*\}', clean_result, re.DOTALL)
    
    if match:
        try:
            parsed_data = json.loads(match.group(0))
            return {
                "data_type": parsed_data.get("data_type"),
                "chart_type": parsed_data.get("chart_type"),
                "db_selected": parsed_data.get("db_selected"),
                "generated_query": parsed_data.get("generated_query"),
                "message_to_user": parsed_data.get("message_to_user")
            }
        except json.JSONDecodeError as e:
            return {"error_log": state.error_log + [f"JSON Parse Error: {str(e)}"], "sql_retries": state.sql_retries + 1}
    else:
        return {"error_log": state.error_log + ["Failed to output a JSON object."], "sql_retries": state.sql_retries + 1}

