# 4. Node 1.5: Context Builder & Summarizer
# ==========================================
async def context_builder_node(state: BIAgentState) -> dict:
    print("📚 Context Builder Node Triggered")
    turn_count = state.turn_count
    chat_history = state.chat_history
    context_summary = state.context_summary
    
    # 1. Summarization Trigger (Evaluates before adding the current turn)
    if turn_count >= 5 and len(chat_history) > 0:
        history_text = "\n".join([f"{msg['role']}: {msg['content']}" for msg in chat_history])
        summary_prompt = get_summarizer_prompt(context_summary, history_text)
        
        response = await router_llm.ainvoke([HumanMessage(content=summary_prompt)])
        context_summary = re.sub(r'<think>.*?</think>', '', response.content, flags=re.DOTALL).strip()
        
        turn_count = 0 
        chat_history = [] 

    # 2. Schema Retrieval using Meta DB Client
    es_query = {"query": {"term": {"selected_topic.keyword": state.current_tab}}}
    schemas_string = ""
    try:
        es_response = await meta_es_client.search(index="db_schemas", body=es_query)
        hits = es_response.get("hits", {}).get("hits", [])
        
        schema_list = [json.dumps(hit["_source"]) for hit in hits]
        schemas_string = "\n---\n".join(schema_list)
    except Exception as e:
        schemas_string = f"Error: Could not retrieve schema. {e}"

    return {
        "turn_count": turn_count,
        "chat_history": chat_history,
        "context_summary": context_summary,
        "schemas": schemas_string
    }

